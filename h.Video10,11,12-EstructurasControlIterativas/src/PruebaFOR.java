
public class PruebaFOR 
{

	public static void main(String[] args) 
	{
		// Estructuras de control: 1. Condicionales (IF-ELSE, SWITCH, ELSE IF) 2. Iterativas
		
		// IF-ELSE (comprobacion) -> IF- A (true) o ELSE- B (false)
		
		// ABCTZ, ABCTZ, ABCTZ, ABCTZ
		
		// FOR, WHILE y DO-WHILE
		
		// FOR: Estructura de control iterativa (realizar una serie de instrucciones de manera repetitiva, en forma bucle)
		
		int numero = 0;
		
		for(int iteracion = 1; iteracion <= 100 ;iteracion++)
		{
			System.out.println("Entrada en la iteración");
			System.out.println("Me encuentro en la iteración número: " + iteracion);
			System.out.println("Salida de la iteración");
			System.out.println("____________________________________");
			System.out.println();
		}
		
		// Salida del for
		System.out.println("SALIDA DEL FOR.");

	}

}
