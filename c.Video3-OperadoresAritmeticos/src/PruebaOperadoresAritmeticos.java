public class PruebaOperadoresAritmeticos
{

	public static void main(String[] args)
	{
		// suma, resta, multiplicación, división y resto.

		// suma: +
		// resta: -
		// división: /
		// resto: %

		// int operandoUno = 10;
		double operandoUno;
		operandoUno = 15.248;
		double operandoDos = 7.95;
		double resultado = 0;

		// Operaciones con variables y demo de los resultados

		// suma
		resultado = operandoUno + operandoDos;
		System.out.println(resultado);

		// resta
		resultado = operandoUno - operandoDos;
		System.out.println(resultado);

		// multiplicación
		resultado = operandoUno * operandoDos;
		System.out.println(resultado);

		// división
		resultado = operandoUno / operandoDos;
		System.out.println(resultado);

		// resto
		resultado = operandoUno % operandoDos;
		System.out.println(resultado);

	}

}
